package Practice_from_videos;

public class PrimitiveDataTypes {

    public static void main(String[] args) {

        //Integer: age, year, salary, miles
        byte age = 35;
        short year = 2000;
        int salary = 100000;  //preferred in java
        long miles = 9999999999L;

        // Decimals: tax, pi
        double tax = 0.26;  //preferred in java
        float pi = 3.14F;

        // symbols: @, #
        char ch1 = '@';
        char ch2 = '#';


        System.out.println(age);
        System.out.println(year);
        System.out.println();
    }
}
